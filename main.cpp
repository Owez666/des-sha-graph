#include <QCoreApplication>
// Подключаем класс QCoreApplication.
// Это "сердце" консольных приложений на Qt.
// Оно запускает цикл обработки событий (event loop).

#include "mytcpserver.h"
// Подключаем наш класс MyTcpServer (в mytcpserver.h/.cpp).
// Этот класс отвечает за работу TCP-сервера.

int main(int argc, char *argv[])
{
    // argc, argv — это стандартные аргументы командной строки.
    // Qt их тоже может использовать (например, для каких-то настроек).

    QCoreApplication a(argc, argv);
    // Создаём объект приложения (обязательно!).
    // Без него Qt не будет работать: события не будут обрабатываться.

    MyTcpServer myserv;
    // Создаём наш сервер.
    // В его конструкторе он сразу запускается и начинает слушать порт.

    return a.exec();
    // Запускаем "цикл событий" (event loop).
    // Теперь программа работает бесконечно и реагирует на события:
    // - если подключился клиент - сработает слот newConnection
    // - если клиент прислал данные - сработает slotServerRead
    // - если клиент отключился - сработает slotClientDisconnected
}
